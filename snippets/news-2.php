                    Following February’s soft launch event Soundsweird: Non-formal Music Learning for Young People at Tempel Amsterdam, we are delighted to announce that the educational platform soundsweird.org is complete! Soundsweird.org is an educational platform for non-formal music learning and experimental approaches to sound for youth in the ages 13 to 20. It contains six free workshop modules, each promoting listening, mutual sound research and co-creation.
                </p>
