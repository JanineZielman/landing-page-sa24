            <h2 class="news-headline">
                Maritime Frictions recordings added to Sonic Acts online archive
            </h2>
            <div class="news-date">
                28 Aug 2023
            </div>
        </div>
        
        
        <div class="news-text-wrapper">
            <div class="news-teaser">
                <p>
                    We are delighted to publish several new videos from Maritime Frictions by Embassy of the North Sea, Fred Carter, and Liquid Time via our YouTube and Vimeo channels. Alongside these, you can also listen to Velma Spell’s live recording of 'About the North Sea' on SoundCloud.
                </p>
            </div>
            
            <div class="news-text closed">
                <p>
                <em>Maritime Frictions</em> was a collaborative event organised by Sonic Acts and FieldARTS’ in Ruigoord, west of Amsterdam. The programme included a listening walk with Lance Laoyan, field presentations from Harpo ’t Hart and Frank Bloem (Embassy of the North Sea), talks by Fred Carter and Charmaine Chua, a performance lecture by Liquid Time (Jacob Bolton and Miriam Matthiessen), a screening of Michaela Büsse’s ‘Building with Nature’ (2022), a sound performance from Velma Spell, and DJ set by Nessim.
                </p>
                <p>
                    Explore the Sonic Acts youtube and soundcloud channels in full, both offering an insight into past and ongoing projects and events within the Sonic Acts community as well as commissioned artworks and collaborations.
