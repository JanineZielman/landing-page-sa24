    <p>
The 30th anniversary edition of the Sonic Acts Biennial takes place from 2 February to 24 March 2024, with an immersive and intensive ‘festival’ weekend running from 23 to 25 February. Unfolding across various temporal modalities and timescales – from month-long exhibitions to hour-long walks – the programme will embrace a multiplicity of ways of sensing, engaging with the thresholds of the body and tuning into otherness. Over the duration of two months, the Biennial will take shape in a multitude of forms: an exhibition, a multichannel sound programme, club nights, large theatrical productions, a symposium, film programmes, sound and video installations, sound and field walks, and workshops, amongst others. Curated around topics and thematics that echo the ongoing environmental crisis and its interconnectedness and tensions with the body, the Biennial programme will take place in various locations around Amsterdam, such as Paradiso, Muziekgebouw, Eye Filmmuseum, W139, Zone2Source, Oude Kerk, Looiersgracht 60, Stedelijk Museum, Garage Noord and kanaal40, amongst others.
    </p>
</div>

<div class="full-text closed">
    <div class="text-span text-span-10">
    </div>
    <p>
    The escalating climate emergency and the role and importance of art within this context has been an ongoing interest and concern for Sonic Acts over the past decade. Together we have asked how art can respond to, and intervene in, the artificially constructed divisions between nature and culture that have fuelled our extractive economies. Simultaneously, we strive to put into question the limits of artistic practices in responding to the climate crisis. Sonic Acts has been invested in presenting alternatives to the ideology that sees the nonhuman as a material resource while rethinking the changes that have unfolded on larger geological timescales – the Holocene morphing into the Anthropocene and the deconstruction of colonial sciences that have shaped dominant Western thought over the past centuries. This 30th anniversary edition will also serve as a vantage point for the Biennial to reflect upon its own role in the ecosystem that Sonic Acts is shaping and inhabiting.
    </p>
    <div class="line-break"></div>
    <div class="text-span text-span-30"></div>
    <p>
    Staying within an ecological framework and embracing the urgency of rethinking relations across species, <em>Sonic Acts Biennial 2024: The Spell of the Sensuous</em> turns for inspiration to, and takes its name from, David Abram’s eponymous book, published around the same time as Sonic Acts was founded.<span id="footnote-1" class="footnote-number">1</span> Under its spell, we will explore the web of relations – experienced and perceived through the ‘sensuous body’ – that evoke our rootedness in the larger ecology of earth beings.<span id="footnote-2" class="footnote-number">2</span>
    </p>

    <div class="line-break"></div>
    <div class="text-span text-span-50">
    </div>
    <p>
        Known today as the author of the ubiquitous term ‘more-than-human’, Abram was one of the first academic ecological thinkers to acknowledge animism as a worldview, drawing attention to cosmologies and traditions that always already regarded the natural world as imbued with consciousness. For Abram, animism is a form of ‘direct perceptual experience, not mediated by technologies’, that brings us in touch with the wider community of the more-than-human world. He contemplated ways of relating – rooted in various Indigenous practices and ancestral knowledge – that emphasised the entanglement of all things on the planet.
    </p>
    <div class="line-break"></div>
    <div class="text-span text-span-20">
    </div>
    <p>
       Adopting a working methodology and curatorial framework that also emphasises relationality and centres the role of intuition and sensing, The Spell of the Sensuous embraces the notion of ‘tuning towards otherness’. This resonates in Alexis Pauline Gumbs’ book Undrowned, where she took her cue from the many marine mammals who echolocate: ‘I had to focus not on what I could see and discern, but instead on where I was in relation, how the sound bouncing off me in relationship to the structures and environments that surround me locates me in a constantly shifting relationship to you, whoever you are by now.’<span id="footnote-3" class="footnote-number">3</span> Extending from these expansive notions of the body, the Biennial programme seeks to develop intersubjective and reciprocal perspectives on relationality.
    </p>
    <div class="line-break"></div>
    <div class="text-span text-span-40">
    </div>

    <p>
       The body is not only human, nor is it ever singular. We will think together with Donna Haraway who, when talking about cyborgs, aptly asked: ‘Why should our bodies end at the skin?’<span id="footnote-4" class="footnote-number">4</span> Departing from her visionary thought, we work towards seeing the body as an open, fluid, and porous entity, with boundaries like membranes, forming a surface of metamorphosis and exchange.
    </p>

    <div class="line-break"></div>
    <div class="text-span text-span-10">
    </div>

    <p>
       But what exactly, and how, do we embody? How are the bodies that we inhabit a manifestation of the world, of the air we are collectively breathing? Systems and practices of extraction, racial capitalism, and neo-colonialism manifest themselves and are carried in the body. Power and violence work through the body and its exhaustion, as Sarah Ahmed writes: ‘The exhaustion of people's capacities to resist; the exhaustion of people’s capacities to live their lives on their own terms; the exhaustion of having to navigate systems that are designed to make it harder to get what you need.’<span id="footnote-5" class="footnote-number">5</span> All bodies are caught up in each other's conditions of life, they cannot ‘provide a respite or escape from history, from infrastructures, from relations of power.’<span id="footnote-6" class="footnote-number">6</span>
    </p>

    <div class="line-break"></div>
    <div class="text-span text-span-30">
    </div>

    <p>
      In thinking through our interconnectedness with the environment, we follow the words of Indigenous scholar Michelle Murphy, who unveils the deep material connection of industrial pollution and our bodies, accentuating the structural inequalities and ‘infrastructures of environmental racism that concentrate chemical violence in some bodies, and security in others.’ Murphy suggests the concept of <em>alterlife</em> – a state of already having been altered by large-scale and everyday environmental violence that is nonetheless a capacity to become something else: ‘Our Bodies are the land. They are of toxic chemicals, and they are something more. They are a manifestation of something bigger, that stretches outward to water, air, ancestors, and other beings, that stretches backwards to messy histories, and forwards to something else.’<span id="footnote-7" class="footnote-number">7</span> Understanding this collective intertwinement can lead to a sense of allegiance; we are responsible for each other, bound together through entanglement. As Sophie Lewis writes in <em>Full Surrogacy Now</em>>, we must come to terms with 'the very actual but also utopian fact that we are the makers of one another, and we can learn to act like it.’<span id="footnote-8" class="footnote-number">8</span>
    </p>

    <div class="line-break"></div>
    <div class="text-span text-span-50">
    </div>

    <p>
       Astrida Neimanis in <em >Bodies of Water</em> also develops a posthuman feminist thought that envisions our bodies as inseparable from the ‘natural world’.<span id="footnote-9" class="footnote-number">9</span> For Neimanis, the element of water ties human beings both to their surroundings and one another. It moves through us as contaminated vapour, as life-giving liquidity, overflowing the human and non-human divide, putting us face to face with our fundamental porousness.
    </p>

    <div class="line-break"></div>
        <div class="text-span text-span-40">
    </div>

    <p>
   In her work, feminist scholar María Puig de la Bellacasa explores the concept of care and mutual touch that extends beyond human interactions – challenging the idea that ‘to care’ is only a human feature. Touching is not only a tactile act, but also carries within it the act of entering into the reciprocal nature of direct perception. To touch is also to feel oneself being touched. To see is to feel seen. We listen and feel ourselves heard.<span id="footnote-10" class="footnote-number">10</span> In <em>Quantum Listening</em>, musician and composer Pauline Oliveros speaks in this context of the field of listening, a sensing field affected in its form by each listener through the act of listening. ‘The form affects the listener in a dance of reflection in the space between… In fact, the whole body listens in this heightened state of awareness.’<span id="footnote-11" class="footnote-number">11</span>
    </p>
    <div class="line-break"></div>
    <div class="text-span text-span-10">
    </div>

    <p>
        How do we tune into that which we cannot grasp with language or vision, forms of cognition which are often positioned as the primary ways of knowing in our societies? Embracing the fluidity of the boundaries between human and more-than-human, the Biennial will expand beyond simply datafying the climate crisis and instead search for the vibrations, intimacies, resonances, and leakages emanating from our own bodies to the bodies of the Earth. In the process, it will not abandon solid ground but, following Abram, we will try to rethink ‘rationality’ and learn how to feel the world and open up the potentialities of senses that can help unlock the not easily accessible connections with our surroundings.
    </p>
    <div class="line-break"></div>
    <div class="text-span text-span-30">
    </div>

    <p>
        The visual identity that has been developed for The <em>Spell of the Sensuous</em> pays homage to Georgia O’Keeffe, an artist renowned for her mythic and uninhabited landscape masterpieces. Channelling her distinctive palette, the AI generated visuals employ earthly and lively hues hinting at images beyond human senses – a mystical entrancement in O’Keeffe’s landscapes or the liquid interconnectedness of (non)beings.
    </p>
    <div class="line-break"></div>
    <div class="text-span text-span-50">
    </div>

    <p>
        In her own words, remarking on the sensuously intimate and familial act of noticing nature, ‘in a way – nobody sees a flower – really – it is so small – we haven’t time – and to see takes time like to have a friend takes time.’<span id="footnote-12" class="footnote-number">12</span>
    </p>

